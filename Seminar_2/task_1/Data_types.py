# Integers (числа)
number_int = 1

# Floats (числа)
number_float = 2.2

# Strings (строки)
word = 'Слово'

# Lists (списки)
list_1 = [15, 12, 19, 25]

dictionaries_1 = {'last_name': 'Ivanov', 'name': 'Ivan',
                  'age': '35'}  # Dictionaries (словари)

# Tuples (кортежи)
tuples_1 = ('Ivanov', 'Ivan', '35')

# Sets (множества)
sets_1 = set([25, 35, 45, 55, 65])

# Frozensets (множества)
frozenset_1 = frozenset([25, 35, 45, 55, 65])

# Boolean (логический тип данных)
boolean_1 = True

print(
    f"\n Тип number_int - {type(number_int)}\n Тип  number_float - {type(number_float)}")
print(f" Тип word - {type(word)}\n Тип list_1 - {type(list_1)}")
print(
    f" Тип dictionaries_1 - {type(dictionaries_1)}\n Тип tuples_1 - {type(tuples_1)}\n Тип sets_1 - {type(sets_1)}")
print(
    f" Тип frozenset_1 - {type(frozenset_1)}\n Тип boolean_1 - {type(boolean_1)}\n")
